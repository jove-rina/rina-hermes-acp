/**
 * Generate media/src/shared/webview-protocol.js from src/shared/webview-protocol.ts
 * so message type constants stay single-sourced.
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.join(fileURLToPath(new URL('.', import.meta.url)), '..');
const tsPath = path.join(root, 'src/shared/webview-protocol.ts');
const outPath = path.join(root, 'media/src/shared/webview-protocol.js');

/** @param {string} source @param {string} name */
function extractConstArray(source, name) {
    const re = new RegExp(`export const ${name} = \\[([\\s\\S]*?)\\] as const;`, 'm');
    const match = source.match(re);
    if (!match) {
        throw new Error(`Could not find export const ${name} in ${tsPath}`);
    }
    const entries = [...match[1].matchAll(/'([^']+)'/g)].map((m) => m[1]);
    if (entries.length === 0) {
        throw new Error(`${name} is empty in ${tsPath}`);
    }
    return entries;
}

/** @param {string} name @param {string[]} items */
function formatArray(name, items) {
    const lines = items.map((item) => `    '${item}',`).join('\n');
    return `export const ${name} = [\n${lines}\n];`;
}

const source = fs.readFileSync(tsPath, 'utf8');
const webviewTypes = extractConstArray(source, 'WEBVIEW_TO_EXTENSION_MESSAGE_TYPES');
const extensionTypes = extractConstArray(source, 'EXTENSION_TO_WEBVIEW_MESSAGE_TYPES');

const output = `// AUTO-GENERATED by scripts/generate-webview-protocol.mjs — do not edit manually.
// Source: src/shared/webview-protocol.ts

/** Webview → extension (HermesChatProvider.onDidReceiveMessage). */
${formatArray('WEBVIEW_TO_EXTENSION_MESSAGE_TYPES', webviewTypes)}

/** Extension → webview (webview message handlers). */
${formatArray('EXTENSION_TO_WEBVIEW_MESSAGE_TYPES', extensionTypes)}

export function isWebviewToExtensionMessageType(type) {
    return WEBVIEW_TO_EXTENSION_MESSAGE_TYPES.includes(type);
}

export function isExtensionToWebviewMessageType(type) {
    return EXTENSION_TO_WEBVIEW_MESSAGE_TYPES.includes(type);
}
`;

fs.writeFileSync(outPath, output);
console.log(`Generated ${path.relative(root, outPath)} (${webviewTypes.length} + ${extensionTypes.length} message types)`);
